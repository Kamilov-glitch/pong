function love.conf(t)
    t.window.title = "Pong Game"
    t.window.width = 800
    t.window.height = 600
    t.window.resizable = false
    t.window.fullscreen = false
    t.window.vsync = 1
    t.window.msaa = 0
    t.window.highdpi = false
    t.window.borderless = false
    t.window.display = 1
end
